            <a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion2" href="#collapseTwo5">
                <font color="000000"><b>How is One-up different from other crowd-funding platforms?</b></font>
            </a>
        </div>
        <div id="collapseTwo5" class="accordion-body collapse">
            <div class="accordion-inner">
                <ol>
                    <li>One-up is solely dedicated to Greek Businesses. We do not fund other types of projects. Our mission is to create social impact by providing seed-capital for new businesses to get started.
                        <br>
                    </li>
                    <li>All donations are handled through <a href="http://www.praksis.gr/el/%CF%80%CF%81%CE%BF%CE%B3%CF%81%CE%AC%CE%BC%CE%BC%CE%B1%CF%84%CE%B1/%CF%84%CF%81%CE%B5%CF%87%CE%BF%CF%85%CF%83%CE%B5%CF%82-%CF%80%CE%B1%CF%81%CE%B5%CE%BC%CE%B2%CE%B1%CF%83%CE%B5%CE%B9%CF%82/item/business-coaching-center" target="_blank">PRAKSIS</a>. In other words, the cash is not provided to the entrepreneur as a lump-sum cash donation. An agreed sum is provided to the entrepreneur as and when they reach specific and agreed milestones. Once these are reached, the specified amount of the funds is released until the entrepreneur reaches his/her next milestone. This is in order to ensure that every cent of your donation - managed by a hugely respected NGO - can hold the entrepreneur to account. The donors in the meantime are informed of all the entrepreneurs’ activities ensuring close monitoring, information and transparency on the project in order to secure, to the best of our ability accountability for, and the most efficient use of, your donation.</li>
                    <li>Successful businesses are provided the opportunity to give back to the community through <a href="http://www.praksis.gr/el/%CF%80%CF%81%CE%BF%CE%B3%CF%81%CE%AC%CE%BC%CE%BC%CE%B1%CF%84%CE%B1/%CF%84%CF%81%CE%B5%CF%87%CE%BF%CF%85%CF%83%CE%B5%CF%82-%CF%80%CE%B1%CF%81%CE%B5%CE%BC%CE%B2%CE%B1%CF%83%CE%B5%CE%B9%CF%82/item/business-coaching-center" target="_blank">PRAKSIS</a> and offer work-placement to individuals who are in real need. In this way we create social impact that immeasurably multiplies the value of your donation. </li>
                </ol>
            </div>
