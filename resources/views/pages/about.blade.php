                    <h3 style="margin: 0; line-height: 26px; margin-bottom: 10px;"><b style="color: rgb(0, 105, 145); font-size: medium;">PRAKSIS<font color="006991"> BCC</font></b><br></h3> One-up is a product of the PRAKSIS Business Coaching Center.
                    <br>
                    <br> PRAKSIS Business Coaching Center functions as a support center for the selected candidates offering training, coaching and consulting in order to help people materialize their entrepreneurial goals.&nbsp; The program provides comprehensive practical training and preparation so that the candidate will be able to compose a conclusive business plan, which will ensure in the best possible way the viability of his business.
                    <br>
                    <br> The methodology used incorporates international trends in business models with the reality of the Greek market. More specifically, the BCC supports participants by offering training, development of personal skills&nbsp;(coaching) and advisory (consulting), to help them reach their business goals and create a small business.
                    <br>
                    <br> Participants are selected through a rigorous process, which is designed to ensure:
                    <br>
                    <br> ·&nbsp;&nbsp;&nbsp; The viability of the business idea
                    <br> ·&nbsp;&nbsp;&nbsp; The social and economic impact of the business idea
                    <br> ·&nbsp;&nbsp;&nbsp; The persistence and determination exhibited by the individual at the prospect of becoming a freelancer
                    <br>
                    <br> In Greece the economic crisis reversed the daily routine of many people, bringing it faces intense livelihood problems, the PRAKSIS BCC aims to help people who have the vision, perseverance, dedication and ability to 'try' and they want a powerful ally in achieving this goal. With a skill development program based on the individual needs of each participant, the ultimate goal is to start or restart a small or medium enterprise.
