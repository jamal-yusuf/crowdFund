                <td valign="top">One-up is a crowd funding platform where young entrepreneurs can upload their campaigns and ask donations from the public in order to gain access to seed capital. &nbsp;Based on the idea, “small amounts from many donors”, donations begin from 5 euros.
                    <br>
                    <br>One-up is a PRAKSIS BCC tool created to fulfil the need that young entrepreneurs have to access capital in order to start their business off.
                    <br>NGO PRAKSIS gathers the money and is responsible to proportionately administer it to the beneficiary campaign holder, in line with the investment plan he/she has provided. &nbsp;This way, and with no commission added, we make sure that the money offered by the donors facilitates the exact purpose for which it has been gathered, until the last euro.
                    <br>One-up is running since December 2015, and until today (* August 2016) has hosted 13 business campaigns and has attracted 7,315 donors from Greece, the USA, the UK, Germany, Cyprus, The Netherlands, Belgium, Spain and France.
