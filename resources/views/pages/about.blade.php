                    <br><b style="color: rgb(0, 105, 145); font-size: medium;"><font color="006991">What We Do</font></b>&nbsp;
                    <br>
                    <br>The PRAKSIS Business Coaching Center is a training, consulting and coaching center which supports entrepreneurship. The One-up platform is an extension of the BCC program providing entrepreneurs with alternative sources of financing, and donors with a new, simple and direct way to create real social impact on the individual, family and local levels. By helping to launch new businesses we support a society in a crisis-torn economy where jobs are limited. New businesses are the life-blood of an economy and at One-up we believe in encouraging local economies to grow, one individual and one business at a time. One-up connects business minds with a community that wants to see new ideas and new businesses thrive.
                    <br>
                    <br>
                    <br><b style="color: rgb(0, 105, 145); font-size: medium;"><font color="006991">How It Started</font></b>&nbsp;
                    <br>
                    <br>The PRAKSIS Business Coaching Center was launched in 2014. The idea, concept, design and creation of the platform came from Paul Kidner who wanted to provide the missing link between the talent of Greek entrepreneurs and alternative sources of local financing&nbsp;but currently find it impossible to get the start they need. The economy has shriveled, loans have dried up and investment in SME’s has tanked. PRAKSIS created the Business Coaching Center, and One-up in order to address those needs and provide new life for businesses in Greece.&nbsp;
